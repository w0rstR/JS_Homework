function createStucture (){
    const wrapper = document.createElement('div')
    wrapper.style.padding='20px'
    wrapper.style.border='5px solid grey'
    wrapper.style.borderRadius='5px'

    coursesArray.forEach(item=>{
        const {title,monthDuration,hourDuration,modules} = item;
        const wrapperItem = document.createElement('div')
        wrapperItem.style.padding='10px'
        wrapperItem.style.border='2px solid grey'
        wrapperItem.style.borderRadius='5px'
        wrapperItem.style.marginBottom='10px'



        const headerBlock = document.createElement('div')
        const headerBlockItem= document.createElement('h1')
        headerBlockItem.innerText=title
        headerBlock.append(headerBlockItem)

        const durationBlock = document.createElement('div')
        durationBlock.style.display='flex'
        durationBlock.style.columnGap='10px'


        const firstDurationItem = document.createElement('div')
        firstDurationItem.innerText=monthDuration;
        firstDurationItem.style.border='1px solid black';
        firstDurationItem.style.flexGrow='1'


        const secondDurationItem = document.createElement('div')
        secondDurationItem.innerText=hourDuration
        secondDurationItem.style.border='1px solid red';
        secondDurationItem.style.flexGrow='2'
        durationBlock.append(firstDurationItem,secondDurationItem)



        const modulesBlock = document.createElement('div')
        const modulesBlockUl=document.createElement('ul')

        modules.forEach(item=>{
            const modulesBlockLi=document.createElement('li')
            modulesBlockLi.textContent=item
            modulesBlockUl.append(modulesBlockLi)
        })

        modulesBlock.append(modulesBlockUl)
        wrapperItem.append(headerBlock,durationBlock,modulesBlock)
        wrapper.append(wrapperItem)

    })

    document.body.append(wrapper)

}
